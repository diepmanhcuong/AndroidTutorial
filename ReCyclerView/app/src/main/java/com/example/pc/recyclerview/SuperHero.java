package com.example.pc.recyclerview;

public class SuperHero {
    private String name;
    private int avarta;

    public SuperHero(String name, int avarta) {
        this.name = name;
        this.avarta = avarta;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAvarta() {
        return avarta;
    }

    public void setAvarta(int avarta) {
        this.avarta = avarta;
    }
}
